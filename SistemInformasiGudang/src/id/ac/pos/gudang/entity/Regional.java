package id.ac.pos.gudang.entity;

/**
 *
 * @author Oyoy
 */
public class Regional {
    
    private String idRegional;
    private String namaRegional;
    private String kodePos;
    private String noTelp;
    private String alamat;

    public String getNoTelp() {
        return noTelp;
    }

    public void setNoTelp(String noTelp) {
        this.noTelp = noTelp;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getKodePos() {
        return kodePos;
    }

    public void setKodePos(String kodePos) {
        this.kodePos = kodePos;
    }

    public String getIdRegional() {
        return idRegional;
    }

    public void setIdRegional(String idRegional) {
        this.idRegional = idRegional;
    }

    public String getNamaRegional() {
        return namaRegional;
    }

    public void setNamaRegional(String regional) {
        this.namaRegional = regional;
    }
    
    
    
    
    
}
